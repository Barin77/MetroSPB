    /**
     * Это самый большой метод в программе и в нем ищется одновременно время и расстояние.
     * Я пытался различными способами разделить этот метод, чтобы отдельно считалось время и расстояние,
     * но адекватно это сделать, к сожалению, невозможно
     *
     * @param start номер точки(метро), от которой начинаем
     * @param finish номер точеи(метро), до которой мы ищем время и расстояние
     * @return выводит в GUI путь и время от начальной точки(метро) до конечной точки(метро)
     */
    public static String count_by_Dijkstra_algoritm(int start, int finish) {
        if (start == -1 || finish == -1){
            return "Неверное название станции";
        }
        boolean good = false; // Эта переменная будет false, пока рассматриваемая точка не станет finish
        int count_seen_dots=0; // Кол-во просмотренных точек
        String now_trip; // Маршрут к рассматриваемой точке (Для составления дальнейшего маршрута и вывода в GUI)
        String[] trips = new String[dots_count]; // Маршруты к рассмотренным точкам
        int now_dot; // Рассматриваемая точка
        Arrays.fill(trips," "); //на всякий случай очищаем массив путей
        ArrayList<Integer> seen_dots = new ArrayList<>();
        int[] now_seen = new int[dots_count]; // В этот массив записываются наикротчайшие пути к точкам
        Arrays.fill(now_seen, inf);
        now_seen[start]=0; // Записываем, что время до точки от которой мы начинаем равно нулю
        trips[start]= Names.get_name(start);

        System.out.println("Запрошен путь от метро "+Names.get_name(start)+" до метро "+Names.get_name(finish));

        while (!good){
            now_dot = min_dot(now_seen,seen_dots);

            if (now_dot == finish){
                good=true;
                break;
            }
            now_trip=trips[now_dot];
            ArrayList<Integer> can_go_dots = can_go(now_dot);
            for (int i:can_go_dots){
                if (!seen_dots.contains(i)){
                    int ves = now_seen[now_dot]+graph[now_dot][i];
                    if (ves<now_seen[i]){
                        now_seen[i] = ves;
                        if (graph[now_dot][i]==5){
                            trips[i] = now_trip+">перейти на станцию "+ Names.get_name(i)+" ("+Names.get_color(i)+")";
                        }else{
                            trips[i] = now_trip+">"+Names.get_name(i); //v1
                        }
                        System.out.println("Путь обновлен: "+trips[i] + "   Время: "+now_seen[i]);
                    }
                }
            }
            seen_dots.add(count_seen_dots,now_dot);
            count_seen_dots +=1;
        }

        String[] tripz = trips[finish].split(">");
        return go_to_text_for_gui(start,finish,now_seen[finish], tripz);
    }

    /**
     * Отформатированный для графического интерфейса маршрут
     * @param start номер точки(метро) от которой ищем расстояние и время
     * @param finish номер точки(метро) до которой ищем расстояние и время
     * @param time время до точки
     * @param tripz маршрут
     * @return отформатированный для графического интерфейса маршрут
     */
    public static String go_to_text_for_gui(int start, int finish, float time, String[] tripz) {

        String finish_trip = "";
        int count = 0 ;
        for (String i : tripz){
            count += 1;
            finish_trip += i;
            if (tripz.length>count){finish_trip += "-->";}
            if ((count % 5 == 0) && (tripz.length != count)){
                finish_trip += "\n-->";
            }
        }

        return ("\nМаршрут от метро " + Names.get_name(start) + " до метро "+
                Names.get_name(finish) + ":\n"+finish_trip + "\n\nВремя: " + time + "мин - " + time*1.25 + "мин");
    }

    /**
     * @param now_seen  массив точек, которые доступны
     * @param seen_dots массив просмотренных точек
     * @return точка в матрице с наикротчайшим расстоянием из доступных
     */
    private static int min_dot(int[] now_seen, ArrayList<Integer> seen_dots){
        int c = 0;
        int min = 999;
        int dot = 0;

        for (int i : now_seen){
            if (!seen_dots.contains(c) && i<min){
                min = i;
                dot = c;
            }
            c++;
        }
        return dot;
    }

